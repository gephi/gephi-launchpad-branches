    private void refreshModel(TimelineModel model) {
        if (model == null) {
            setEnabled(false);
            setActivated(false);
            return;
        }
        setEnabled(true);
        setActivated(false); // auto-enable the timeline if a model is loaded
        ((TimelineDrawer) drawerPanel).setModel(model);
    }


